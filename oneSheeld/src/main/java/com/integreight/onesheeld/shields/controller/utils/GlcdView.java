    public void drawLine(float x1,float y1,float x2,float y2,int color) {
        float deltaX, deltaY, x, y;
        boolean steep;
        float error, yStep;
        if (x1 > glcdWidth) x1 = 0;
        if (x2 > glcdWidth) x2 = 0;
        if (y1 > glcdHeight) y1 = 0;
        if (y2 > glcdHeight) y1 = 0;

        steep = absDiff(y1, y2) > absDiff(x1, x2);
        if (steep) {
            float temp0 = y1;
            y1 = x1;
            x1 = temp0;
            float temp1 = y2;
            y2 = x2;
            x2 = temp1;
        }
        if (x1 > x2) {
            float temp0 = x1;
            x1 = x2;
            x2 = temp0;
            float temp1 = y1;
            y1 = y2;
            y2 = temp1;
        }

        deltaX = x2 - x1;
        deltaY = absDiff(y1, y2);
        error = deltaX / 2;
        y = y1;
        if (y1 < y2)
            yStep = 1;
        else
            yStep = -1;

        for (x = x1; x <= x2; x++) {
            if (steep)
                setPixel((int) y, (int) x, color);
            else
                setPixel((int) x, (int) y, color);
            error -= deltaY;
            if (error < 0) {
                y = y + yStep;
                error += deltaX;
            }
        }
    }

